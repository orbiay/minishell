#include "../minishell.h"

int	get_var_len(char *var)
{
	int	i;

	i = 0;
	while (var[i] && (var[i] != '=' && var[i] != '+'
			&& var[i] != ' ' && var[i] != '$'
			&& var[i] != '\'' && var[i] != '\"'))
		i++;
	return (i);
}
